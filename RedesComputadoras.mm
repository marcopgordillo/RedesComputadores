<node TEXT="Jerarquía de Protocolos" ID="ID_717086020" CREATED="1633627980131" MODIFIED="1633629156221" TEXT_SHORTENED="true"><richcontent CONTENT-TYPE="xml/" TYPE="DETAILS">
<html>
  <head>
    
  </head>
  <body>
    <ul>
      <li>
        Reducir la complejidad en el diseño.
      </li>
      <li>
        Stack de capas o niveles.
      </li>
      <li>
        El número de redes, el nombre, contenido difieren en diferentes redes.
      </li>
      <li>
        El propósito de cada capa es ofrecer ciertos servicios a la capa superior mientras se encapsula los detalles de la capa de como los servicios ofrecidos están implementados.
      </li>
      <li>
        Este concepto también es conocido en la ciencia como ocultamiento de información, <b>encapsulación</b>, abstracción de datos o programación orientada a objetos.
      </li>
      <li>
        <b>Protocolo</b>: Las convenciones usadas en una conversación entre un host en una capa n con otro host en la misma capa n. Es un acuerdo.
      </li>
      <li>
        Los datos no son transferidos directamente de una capa n a otra capa n, sino que cada capa pasa información de datos y control a la capa inmediatamente inferior hasta alcanzar la última capa inferior.
      </li>
      <li>
        La última capa es la <b>capa física</b>, capa 1 o medio físico donde la comunicación realmente ocurre.
      </li>
      <li>
        Entre cada par de capas adyacentes está una <b>interfaz</b>, que consiste que operaciones y servicios la capa inferior dispone para la capa superior.
      </li>
      <li>
        Cada capa realiza unas funciones <b>bien conocidas</b>.
      </li>
      <li>
        Las interfaces permiten reemplazar las diferentes tecnologías de capas inferiores con implementaciones completamente diferentes.
      </li>
    </ul>
  </body>
